/************************************************
题目描述：
    输入一个数n，然后输入n个数值各不相同，再输入一个值为x，输出这个值在这个数组中的下标（从0开始，若不在数组中则输出-1）
输入：
    测试数据有多组，输入n(1<=n<=200),接着输入n个数，然后输入x。
输出：
    对于每个输入，请输出结果。
样例输入：
2
1 3
0
样例输出：
-1

************************************************/
#include<stdio.h>

int main(){
    int buf[200];
    int n;
    while(scanf("%d",&n) != EOF){
        for(int i=0;i<n;i++){
            scanf("%d",&buf[i]);
        }//输入数据
        int x,ans=-1;   //初始化为-1，以期在找不到答案时能正确的输出-1
        scanf("%d",&x);
        for(int i=0;i<n;i++){//依次遍历数组元素
            if(x==buf[i]){
                ans = i;
                break;
            }
        }
        printf("%d\n",ans);
    }
    return 0;
}








